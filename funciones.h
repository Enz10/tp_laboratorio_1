
/** \brief Ingreso de numeros
 * \param
 * \return El numero ingresado
 */
float ingresoNumero();


/** \brief Recibe dos numeros enteros o flotantes y los suma
 * \param num1 y num2 corresponden al primer y segundo numero recibidos
 * \return La suma de los dos numeros
 */
float suma(float num1,float num2);


/** \brief Recibe dos numeros enteros o flotantes y los resta
 * \param num1 y num2 corresponden al primer y segundo numero recibidos
 * \return La resta de los dos numeros
 */
float resta(float num1, float num2);


/** \brief Recibe dos numeros enteros o flotantes y los divide
 * \param num1 y num2 corresponden al primer y segundo numero ingresados
 * \return La division de los dos numeros
 */
float division( float num1, float num2);

/** \brief Recibe un numero y valida si es distinto a cero
 * \param num2 corresponde al segundo numero ingresado
 * \return Flag en 0 si el num2 es cero
 */
float divisionCero(float num2);


/** \brief Recibe dos numeros enteros o flotantes y los multiplica
 * \param num1 y num2 corresponden al primer y segundo numero ingresado
 * \return La multiplicacion de los dos numeros
 */
float multiplicacion(float num1, float num2);


/** \brief Recibe un numero y hace el factorial de este
 * \param n corresponde al numero ingresado
 * \return El factorial del numero ingresado
 */
float factorial(float n);

/** \brief Recibe un numero y valida que solo ingresen enteros
 * \param num corresponde al numero ingresado
 * \return La validacion 1 si es entero o 0 si es decimal
 */
float validacionDecimal (float num);

/** \brief Recibe un numero entero o flotante y valida si es positivo
 * \param num corresponde al numero ingresando y back al valor del flag
 * \return El valor si es 1 es positivo si es entero 0
 */
float validacionPositivos(float num);

